package com.laby.framework.codegen.core.enums;

import lombok.AllArgsConstructor;
import lombok.Getter;

/**
 * 业务编码前缀枚举
 * <p>
 * 定义 WMS 系统中所有业务编码的前缀、描述和序列号长度
 * <p>
 * 编码格式：前缀 + 日期(yyyyMMdd) + 序列号
 * 例如：IN20251030001（入库单）、WH2025103001（仓库）
 *
 * @author laby
 */
@Getter
@AllArgsConstructor
public enum CodePrefixEnum {

    // ==================== 仓库管理 ====================
    /** 仓库编码 */
    WAREHOUSE("WH", "仓库编码", 3),
    /** 库区编码 */
    WAREHOUSE_AREA("WA", "库区编码", 3),
    /** 库位编码 */
    WAREHOUSE_LOCATION("LOC", "库位编码", 4),

    // ==================== 商品管理 ====================
    /** 商品SKU编码 */
    GOODS_SKU("SKU", "商品SKU编码", 5),
    /** 商品分类编码 */
    GOODS_CATEGORY("CAT", "商品分类编码", 3),

    // ==================== 入库管理 ====================
    /** 入库单号 */
    INBOUND("IN", "入库单号", 5),

    // ==================== 出库管理 ====================
    /** 出库单号 */
    OUTBOUND("OUT", "出库单号", 5),

    // ==================== 库存管理 ====================
    /** 库存编号 */
    INVENTORY("INV", "库存编号", 5),

    // ==================== 拣货管理 ====================
    /** 拣货波次号 */
    PICKING_WAVE("WAVE", "拣货波次号", 4),
    /** 拣货任务号 */
    PICKING_TASK("PICK", "拣货任务号", 5),

    // ==================== 盘点管理 ====================
    /** 盘点计划号 */
    STOCK_TAKING_PLAN("STP", "盘点计划号", 4),
    /** 盘点单号 */
    STOCK_TAKING("ST", "盘点单号", 5),

    // ==================== 移库管理 ====================
    /** 移库单号 */
    STOCK_MOVE("MOVE", "移库单号", 4),

    // ==================== 主数据管理 ====================
    /** 供应商编码 */
    SUPPLIER("SUP", "供应商编码", 4),
    /** 客户编码 */
    CUSTOMER("CUS", "客户编码", 4),
    /** 承运商编码 */
    CARRIER("CAR", "承运商编码", 4),

    // ==================== 其他 ====================
    /** 批次号 */
    BATCH_NO("BATCH", "批次号", 4),
    /** 序列号 */
    SERIAL_NO("SN", "序列号", 6),
    /** 容器编号 */
    CONTAINER("CTN", "容器编号", 4),
    /** 托盘编号 */
    PALLET("PLT", "托盘编号", 4);

    /**
     * 编码前缀
     */
    private final String prefix;

    /**
     * 编码描述
     */
    private final String description;

    /**
     * 序列号长度（每日从 1 开始递增）
     * <p>
     * 例如：
     * - 3位：001-999（最多999条/天）
     * - 4位：0001-9999（最多9999条/天）
     * - 5位：00001-99999（最多99999条/天）
     * - 6位：000001-999999（最多999999条/天）
     */
    private final Integer serialLength;
}


